#include <stdlib.h>

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ptr;

	i = 0;
	while (*s)
	{
		s++;
		i++;
	}
	while (i > 0 && *s != c)
	{
		s--;
		i--;
	}
	if (*s == c)
	{
		ptr = (char *)s;
		return (ptr);
	}
	return (NULL);
}
